// Russian word boundary helpers — JS \b doesn't recognize Cyrillic as word chars
const RB = '(?<![а-яёА-ЯЁa-zA-Z0-9_])';  // left word boundary
const RE = '(?![а-яёА-ЯЁa-zA-Z0-9_])';   // right word boundary
const RL = '[а-яёА-ЯЁ]';

// Full word match (both boundaries)
function rw(word, flags = 'gi') {
  return new RegExp(`${RB}${word}${RE}`, flags);
}

// Stem match — right boundary only, allows Russian verb prefixes (за-, пере-, от-, etc.)
function rp(word, flags = 'gi') {
  return new RegExp(word + RE, flags);
}

// Plain regex from string, no boundaries added
function r(src, flags = 'gi') {
  return new RegExp(src, flags);
}

